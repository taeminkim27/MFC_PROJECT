// CMFCApplication2Dlg 메시지 처리기

BOOL CMFCApplication2Dlg::OnInitDialog()
{
	CDialogEx::OnInitDialog();

	// 시스템 메뉴에 "정보..." 메뉴 항목을 추가합니다.

	// IDM_ABOUTBOX는 시스템 명령 범위에 있어야 합니다.
	ASSERT((IDM_ABOUTBOX & 0xFFF0) == IDM_ABOUTBOX);
	ASSERT(IDM_ABOUTBOX < 0xF000);

	CMenu* pSysMenu = GetSystemMenu(FALSE);
	if (pSysMenu != nullptr)
	{
		BOOL bNameValid;
		CString strAboutMenu;
		bNameValid = strAboutMenu.LoadString(IDS_ABOUTBOX);
		ASSERT(bNameValid);
		if (!strAboutMenu.IsEmpty())
		{
			pSysMenu->AppendMenu(MF_SEPARATOR);
			pSysMenu->AppendMenu(MF_STRING, IDM_ABOUTBOX, strAboutMenu);
		}
	}

	// 이 대화 상자의 아이콘을 설정합니다.  응용 프로그램의 주 창이 대화 상자가 아닐 경우에는
	//  프레임워크가 이 작업을 자동으로 수행합니다.
	SetIcon(m_hIcon, TRUE);			// 큰 아이콘을 설정합니다.
	SetIcon(m_hIcon, FALSE);		// 작은 아이콘을 설정합니다.

	// TODO: 여기에 추가 초기화 작업을 추가합니다.
	SetDlgItemText(IDC_EDIT1, _T(""));

	return TRUE;  // 포커스를 컨트롤에 설정하지 않으면 TRUE를 반환합니다.
}

void CMFCApplication2Dlg::OnSysCommand(UINT nID, LPARAM lParam)
{
	if ((nID & 0xFFF0) == IDM_ABOUTBOX)
	{
		CAboutDlg dlgAbout;
		dlgAbout.DoModal();
	}
	else
	{
		CDialogEx::OnSysCommand(nID, lParam);
	}
}

// 대화 상자에 최소화 단추를 추가할 경우 아이콘을 그리려면
//  아래 코드가 필요합니다.  문서/뷰 모델을 사용하는 MFC 애플리케이션의 경우에는
//  프레임워크에서 이 작업을 자동으로 수행합니다.

void CMFCApplication2Dlg::OnPaint()
{
	
	if (IsIconic())
	{
		CDialogEx::OnPaint();
	}
	else
	{
		CPaintDC dc(this);

		
		CRect rect;
		GetClientRect(&rect);
		dc.FillSolidRect(rect, GetSysColor(COLOR_BTNFACE));

		
		CString strRadius;
		GetDlgItemText(IDC_EDIT1, strRadius);
		int radius = _ttoi(strRadius);

		int maxX = 500;
		int maxY = 400;
		int centerX = rand() % maxX + 50;
		int centerY = rand() % maxY + 50;

		CPen yellowPen(PS_SOLID, 2, RGB(255, 255, 0));
		dc.SelectObject(&yellowPen);
		dc.SelectStockObject(NULL_BRUSH); 

		dc.Ellipse(centerX - radius, centerY - radius, centerX + radius, centerY + radius);
		CPen redPen(PS_SOLID, 2, RGB(255, 0, 0));
		dc.SelectObject(&redPen);
	
		dc.MoveTo(centerX - 10, centerY);
		dc.LineTo(centerX + 10, centerY);
		dc.MoveTo(centerX, centerY - 10);
		dc.LineTo(centerX, centerY + 10);
	}
}

